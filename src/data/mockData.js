export const toStatusClass = (value) => String(value).toLowerCase().replace(/[\s_]+/g, '-')

export const formatDateTime = (value) => {
  return new Date(value).toLocaleString('en-IN', {
    day: '2-digit',
    month: 'short',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  })
}

export const getOrderQuantity = (order) => order.line_items.reduce((sum, item) => sum + item.quantity, 0)

export const getOrderNet = (order) => order.total_price - order.total_refunded

export const getProductInventory = (product) => product.variants.reduce((sum, variant) => sum + variant.inventory_quantity, 0)

export const getPriceRange = (product) => {
  const prices = product.variants.map((variant) => variant.price)
  return {
    min: Math.min(...prices),
    max: Math.max(...prices),
  }
}
